
#Câu 6: Tính chi phí sinh hoạt hàng tháng của bạn
print("\nCâu 6: Tính chi phí sinh hoạt hàng tháng của bạn")
rent = int(input("Nhập chi phí thuê nhà: "))
food = int(input("Nhập chi phí ăn uống: "))
transport = int(input("Nhập chi phí đi lại: "))
other = int(input("Nhập chi phí khác: "))
total = rent + food + transport + other
print("Tổng chi phí sinh hoạt hàng tháng của bạn là:", total)